    private void btnContadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContadorActionPerformed
        // TODO add your handling code here:
        //Pega o inicio
        int n = sliInicio.getValue();
        //Pega o Fim
        int fim = sliFim.getValue();
        //Pega o passo
        int p = sliPasso.getValue();
        
        DefaultListModel lista = new DefaultListModel();
       //Condicao que vai desenvolver a logica da atividade
        for(int i = n; i <= fim; i+= p){
            lista.addElement(i);
        }
        //Pega o valor criado e coloca na Lista
        lstContador.setModel(lista);
        
    }//GEN-LAST:event_btnContadorActionPerformed

